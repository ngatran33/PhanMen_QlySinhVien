    private void btnThemsvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemsvActionPerformed
        Sv.getConnect();
        if(SVtxtmasv.getText().isEmpty()||SVtxtmalop.getText().isEmpty() || Svtxtqq.getText().isEmpty()|| SVtxtqt.getText().isEmpty()
            || SVtxtns.getText().isEmpty()||SVtxthoten.getText().isEmpty()|| SVtxtCmtnd.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Thêm thất bại. Không được để trống.");
        }
        else if(Sv.checkMaSV(SVtxtmasv.getText())){
            JOptionPane.showMessageDialog(null, "Mã sinh viên đã tồn tại.");
            SVtxtmasv.requestFocus();
        }
        else if(Sv.checkCmtnd(SVtxtCmtnd.getText())){
            JOptionPane.showMessageDialog(null, "CMTND đã tồn tại");
            SVtxtCmtnd.requestFocus();
        }
        else if(!Pattern.matches("[S][V]\\d{4}", SVtxtmasv.getText())) {JOptionPane.showMessageDialog(null, "Thêm thất bại. Mã SV cần bắt đầu bằng SV theo sau là 4 số.");
            SVtxtmasv.requestFocus();
        }
        else if(!Pattern.matches("[0-9]{10,10}", SVtxtCmtnd.getText())) {
            JOptionPane.showMessageDialog(null, "Thêm thất bại. Cmtnd phải là 10 chữ số.");
            SVtxtCmtnd.requestFocus();
        }
        else if(!Pattern.matches("^\\d{4}[-]\\d{2}[-]\\d{2}$", SVtxtns.getText())) {
            JOptionPane.showMessageDialog(null, "Thêm thất bại. Ngày sinh phải có định dạng năm-tháng-ngày.");
            SVtxtns.requestFocus();
        }
        else if(!Sv.checkMaLop(SVtxtmalop.getText())){ JOptionPane.showMessageDialog(null, "Thêm thất bại. Không tồn tại mã lớp");
            SVtxtmalop.requestFocus();
        }
        else{
            String gt;
            if(rdnam.isSelected()){
                gt=rdnam.getText();
            }
            else
            gt=rdnu.getText();
            if(Sv.add(SVtxtmasv.getText(), SVtxtmalop.getText(), SVtxthoten.getText(), gt, Svtxtqq.getText(),
                SVtxtqt.getText(), SVtxtns.getText(), SVtxtCmtnd.getText())>0){
                JOptionPane.showMessageDialog(null, "Thêm Thành công");
            }else{
                JOptionPane.showMessageDialog(null, "Thêm thất bại: Sai định dạng ngàyl");
            }
            loadDataSV();
            clearTextSv();
            try {
                Sv.closeConnect();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Không đóng được csdl");
            }
        }
    }//GEN-LAST:event_btnThemsvActionPerformed

