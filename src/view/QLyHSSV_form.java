    private void btnXoasvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoasvActionPerformed
        Sv.getConnect();
        if(SVtxtmasv.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Không được để trống masv");
        }else{
        try {
            if(!Sv.checkMaSV(SVtxtmasv.getText())){
                JOptionPane.showMessageDialog(null, "Không tồn tại maSv: "+ SVtxtmasv.getText());
            }
            else if(Sv.checkMaSVDiem(SVtxtmasv.getText())) 
                JOptionPane.showMessageDialog(null, "Sinh viên "+ SVtxtmasv.getText()+" đã có điếm không xóa được");
            else{
                int ref=JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa sv "+SVtxtmasv.getText(),"Xóa",JOptionPane.YES_NO_OPTION );
                if(ref==JOptionPane.YES_OPTION){
                    if(Sv.remove(SVtxtmasv.getText())>0)
                    JOptionPane.showMessageDialog(null, "Xóa Thành công");
                    else{
                         JOptionPane.showMessageDialog(null, "Xóa thất bại");
                    }
                }
            }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Xóa thất bại");
            }
        }
            clearTextSv();
            loadDataSV();
            try {
                Sv.closeConnect();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Không đóng được csdl");
            }
            
    }//GEN-LAST:event_btnXoasvActionPerformed

